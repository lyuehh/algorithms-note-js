puts "把最后一位变成1(x | 1): #{j_4}"

j_5 = (j ^ 1).to_s(2)
puts "最后一位取反(x ^ 1): #{j_5}"

j_6 = (j | (1 << (5-1))).to_s(2)
puts "将从右往左第5位变成1(x | (1 << (5-1))): #{j_6}"

j_7 = (j & ~(1 << (4-1))).to_s(2)
puts "将从右往左第4位变成0(x & ~(1 << (4-1))): #{j_7}"

j_8 = (j ^ (1 << (3-1))).to_s(2)
puts "将从右往左第3位取反(j ^ (1 << (3-1))): #{j_8}"

j_9 = (j ^ (1 << (3-1))).to_s(2)
puts "将从右往左第3位取反(j ^ (1 << (3-1))): #{j_9}"
