#!/usr/bin/env ruby

i = 45
puts "原值: #{i}"

i_b = i.to_s(2)
puts "二进制表示: #{i_b}"

i_1 = (i >> 1).to_s(2)
puts "去掉最后一位(x >> 1): #{i_1}"

i_2 = (i << 1).to_s(2)
puts "最后加一个0(x << 1): #{i_2}"

i_3 = ((i << 1) + 1).to_s(2)
puts "最后加一个1(x << 1 + 1): #{i_3}"

i = 45
i_4 = ((i | 1) - 1).to_s(2)
puts "把最后一位变成0((x | 1) - 1): #{i_4}"
puts ''

j = 46
j_b = 46.to_s(2)
puts "原值: #{j}"
puts "二进制表示: #{j_b}"

